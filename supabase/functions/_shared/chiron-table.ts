/**
 * Get Chiron's ecliptic longitude for a given date via linear interpolation.
 * Returns null if date is outside the 1990-2050 range.
 */
export function getChironLongitude(date: Date): number | null {
  const year = date.getUTCFullYear();
  const month = date.getUTCMonth() + 1;
  const day = date.getUTCDate();

  const daysInMonth = new Date(year, month, 0).getDate();
  const frac = (day - 1) / daysInMonth;

  const idx = CHIRON_DATA.findIndex(([y, m]) => y === year && m === month);
  if (idx === -1) return null;

  const lon1 = CHIRON_DATA[idx][2];
  const nextIdx = idx + 1;
  if (nextIdx >= CHIRON_DATA.length) return lon1;
  const lon2 = CHIRON_DATA[nextIdx][2];

  let diff = lon2 - lon1;
  if (diff > 180) diff -= 360;
  if (diff < -180) diff += 360;

  let result = lon1 + diff * frac;
  if (result < 0) result += 360;
  if (result >= 360) result -= 360;
  return result;
}
